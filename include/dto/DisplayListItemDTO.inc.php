<?php
include_once 'include/dto/AbstractDTO.inc.php';

abstract class DisplayListItemDTO extends AbstractDTO
{
    protected $nom;
    protected $description;
    protected $photo;

    /**
     * @return mixed
     */
    public abstract function getId();

    public abstract  function getDetailUrl();

    /**
     * @return mixed
     */
    public function getNom()
    {
        return $this->nom;
    }

    /**
     * @param mixed $nom
     */
    public function setNom($nom)
    {
        $this->nom = $nom;
    }

    /**
     * @return mixed
     */
    public function getDescription()
    {
        return $this->description;
    }

    /**
     * @param mixed $description
     */
    public function setDescription($description)
    {
        $this->description = $description;
    }

    /**
     * @return mixed
     */
    public function getPhoto()
    {
        return $this->photo;
    }

    /**
     * @param mixed $photo
     */
    public function setPhoto($photo)
    {
        $this->photo = $photo;
    }

}
?>
